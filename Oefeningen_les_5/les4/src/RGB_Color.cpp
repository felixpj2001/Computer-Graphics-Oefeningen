#include "RGB_Color.h"

//////////////////////////////////////////////////////////////////////
// Construction/Destruction
//////////////////////////////////////////////////////////////////////
//--------------------------------------------------------------------

RGB_Color::RGB_Color()
{

}

//--------------------------------------------------------------------

RGB_Color::RGB_Color(float R, float G, float B)
{
	r = R;
	g = G;
	b = B;
}

//--------------------------------------------------------------------

RGB_Color::~RGB_Color()
{

}

//--------------------------------------------------------------------

void RGB_Color::SetRGB(float R, float G, float B)
{
	r = R;
	g = G;
	b = B;
}

//--------------------------------------------------------------------

void RGB_Color::RespectBoundaries()
{
	if(r < 0.0f)
		r = 0.0f;
	else if(r > 1.0f)
		r = 1.0f;
	
	if(g < 0.0f)
		g = 0.0f;
	else if(g > 1.0f)
		g = 1.0f;

	if(b < 0.0f)
		b = 0.0f;
	else if(b > 1.0f)
		b = 1.0f;
}

